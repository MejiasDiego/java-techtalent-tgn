package UD4;

public class T04_3App {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;

		int n = 5;
		int m = 10;

		System.out.println("El valor de x: " + (x));
		System.out.println("El valor de y: " + (y));

		System.out.println("La suma de x e y: " + (x + y));
		System.out.println("La diferencia de x e y: " + (x - y));
		System.out.println("El producto de x por y: " + (x * y));
		System.out.println("El cociente de x entre y: " + (x / y));
		System.out.println("El resto de x entre y: " + (x % y));
		//
		System.out.println("La suma de n e m: " + (n + m));
		System.out.println("La diferencia de n e m: " + (n - m));
		System.out.println("El producto de n por m: " + (n * m));
		System.out.println("El cociente de n entre m: " + (n / m));
		System.out.println("El resto de n entre m: " + (n % m));
		//
		System.out.println("El doble de x " + (x * 2));
		System.out.println("El doble de x " + (y * 2));
		System.out.println("El doble de x " + (n * 2));
		System.out.println("El doble de x " + (m * 2));
		//
		System.out.println("La suma de todas las variables: " + (x + y + n + m));
		System.out.println("El producto de todas las variables: " + (x * y * n * m));

	}

}
