package UD5;

public class UD5_07 {
	public static void main(String[] args) {
        // Inicializar la variable de inicio
        int numero = 1;

        // Utilizar un bucle while para imprimir los números del 1 al 100
        while (numero <= 100) {
            System.out.println(numero);
            numero++; // Incrementar la variable para pasar al siguiente número
        }
	}
}